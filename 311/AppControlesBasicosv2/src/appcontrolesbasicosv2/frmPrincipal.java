    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        double num1, num2, resultado;
        
        try {
            
            num1 =  Double.parseDouble(txtNum1.getText()); // num1 = double.parse(txtNum1.Text);
            num2 = Double.parseDouble(txtNum2.getText());

            resultado =  num1 + num2;

            lblResultado.setText("El resultado es: "+resultado);
            
            
            //Logica de RadiosButons
            if (jradioRestar.isSelected()) {
                JOptionPane.showMessageDialog(null, "Resultado: "+(num1 - num2));
                
                model.addRow(new Object[]{txtNum1.getText(),txtNum2.getText(),"Restar",(num1 - num2),cmbNombres.getSelectedItem()});
                TablaOperaciones.setModel(model);
            }
            if(jradioMultiplicar.isSelected()){
               JOptionPane.showMessageDialog(null, "Resultado: "+(num1 * num2));
                model.addRow(new Object[]{txtNum1.getText(),txtNum2.getText(),"Multiplicar",(num1 * num2),cmbNombres.getSelectedItem()});
                TablaOperaciones.setModel(model);
            }
            
            

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: "+e.getMessage());
        }
        
       
        
    }//GEN-LAST:event_btnCalcularActionPerformed

